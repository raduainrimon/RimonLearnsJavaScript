let num = 89;
if (num >= 90 && num <= 100) {
  console.log("STAR");
} else if (num >= 80 && num <= 89) {
  console.log("A+");
} else if (num >= 70 && num <= 79) {
  console.log("A");
} else if (num >= 60 && num <= 69) {
  console.log("A-");
} else if (num >= 50 && num <= 59) {
  console.log("C");
} else if (num >= 33 && num <= 49) {
  console.log("D");
} else {
  console.log("Fail");
}
